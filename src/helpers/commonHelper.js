export function createSlug(str) {
  str = removeDiacritics(str)
  str = str.toLowerCase()
  str = str.replace(/[^a-z0-9\s-]/g, '')
  str = str.replace(/\s+/g, '-')
  str = str.replace(/-+/g, '-')
  return str
}

